public class DikdortgenHesaplama {
    public static void main(String[] args) {
        double en = 4.5;
        double boy = 7.9;

        double alan = en * boy;
        double cevre = 2 * (en + boy);

        System.out.println("Dikdörtgenin Alanı: " + alan);
        System.out.println("Dikdörtgenin Çevresi: " + cevre);
    }
}
